package Criminal_Data;

public class Criminal {

	String name;
	int age;
	int crime_level;
	String date;
	String state;
	
	public Criminal(String name1, int age1, int crime_level1,String date1,String state1)
	{
		this.name = name1;
		this.age = age1;
		this.crime_level = crime_level1;
		this.date = date1;
		this.state = state1;
	}
}

